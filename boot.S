.section .text.boot
    .global _isr_handler
    .global _start

_isr_handler:
    j   _isr_routine
    j   _start

_isr_routine:
    addi sp, sp, -128       /* 16-byte aligned frame (30 regs x 4 = 120, pad to 128) */
    sw x1,    0(sp)
    sw x3,    4(sp)
    sw x4,    8(sp)
    sw x5,   12(sp)
    sw x6,   16(sp)
    sw x7,   20(sp)
    sw x8,   24(sp)
    sw x9,   28(sp)
    sw x10,  32(sp)
    sw x11,  36(sp)
    sw x12,  40(sp)
    sw x13,  44(sp)
    sw x14,  48(sp)
    sw x15,  52(sp)
    sw x16,  56(sp)
    sw x17,  60(sp)
    sw x18,  64(sp)
    sw x19,  68(sp)
    sw x20,  72(sp)
    sw x21,  76(sp)
    sw x22,  80(sp)
    sw x23,  84(sp)
    sw x24,  88(sp)
    sw x25,  92(sp)
    sw x26,  96(sp)
    sw x27, 100(sp)
    sw x28, 104(sp)
    sw x29, 108(sp)
    sw x30, 112(sp)
    sw x31, 116(sp)

    csrr    t0, mcause
    bltz    t0, .external_irq

    mv      a6, t0              /* a6 = mcause */
    li      t1, 11
    beq     t0, t1, .skip_args  /* ecall: a0-a7 already set by caller */
    csrr    a0, mepc            /* other exceptions: a0 = faulting PC */
.skip_args:
    jal     handle_exception
    csrr    t0, mepc
    addi    t0, t0, 4
    csrw    mepc, t0
    j       .restore

.external_irq:
    li      t1, 0x7FFFFFFF
    and     a0, t0, t1          /* a0 = IRQ number (mcause with bit 31 cleared) */
    jal     handle_interrupt

.restore:
    lw x1,    0(sp)
    lw x3,    4(sp)
    lw x4,    8(sp)
    lw x5,   12(sp)
    lw x6,   16(sp)
    lw x7,   20(sp)
    lw x8,   24(sp)
    lw x9,   28(sp)
    lw x10,  32(sp)
    lw x11,  36(sp)
    lw x12,  40(sp)
    lw x13,  44(sp)
    lw x14,  48(sp)
    lw x15,  52(sp)
    lw x16,  56(sp)
    lw x17,  60(sp)
    lw x18,  64(sp)
    lw x19,  68(sp)
    lw x20,  72(sp)
    lw x21,  76(sp)
    lw x22,  80(sp)
    lw x23,  84(sp)
    lw x24,  88(sp)
    lw x25,  92(sp)
    lw x26,  96(sp)
    lw x27, 100(sp)
    lw x28, 104(sp)
    lw x29, 108(sp)
    lw x30, 112(sp)
    lw x31, 116(sp)
    addi sp, sp, 128
    mret

_start:
    csrw    mie, x0

    la      t0, _isr_handler
    csrw    mtvec, t0

    la      gp, __global_pointer
    la      sp, _stack_end

    la      t0, _bss_start
    la      t1, _bss_end
    beq     t0, t1, .bss_done
.bss_loop:
    sw      zero, 0(t0)
    addi    t0, t0, 4
    blt     t0, t1, .bss_loop
.bss_done:
    call    main

loop:   j loop
